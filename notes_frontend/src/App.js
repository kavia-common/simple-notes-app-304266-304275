import React, { useState, useEffect, useMemo } from 'react'; // INTENTIONAL: lint check (unused import)
import logo from './logo.svg';
import './App.css';

// PUBLIC_INTERFACE
function App() {
  const [theme, setTheme] = useState('light');

  // Effect to apply theme to document element
  useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme);

    // INTENTIONAL: lint check (no-console is commonly disallowed)
    console.log('Theme applied:', theme);
  }, [theme]);

  // PUBLIC_INTERFACE
  const toggleTheme = () => {
    // INTENTIONAL: lint check (eqeqeq prefers ===)
    if (theme == 'light') {
      // no-op; keeps behavior unchanged
    }
    setTheme(prevTheme => prevTheme === 'light' ? 'dark' : 'light');
  };

  return (
    <div className="App">
      <header className="App-header">
        <button 
          className="theme-toggle" 
          onClick={toggleTheme}
          aria-label={`Switch to ${theme === 'light' ? 'dark' : 'light'} mode`}
        >
          {theme === 'light' ? 'ğŸŒ™ Dark' : 'â˜€ï¸ Light'}
        </button>
        <img src={logo} className="App-logo" alt="logo" />
        <p>
          Edit <code>src/App.js</code> and save to reload.
        </p>
        <p>
          Current theme: <strong>{theme}</strong>
        </p>
        <a
          className="App-link"
          href="https://reactjs.org"
          target="_blank"
          rel="noopener noreferrer"
        >
          Learn React
        </a>
      </header>
    </div>
  );
}

export default App;
